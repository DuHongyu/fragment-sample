import com.example.fragmenttest.fragmentImp.ICallBack;

/**
 * @author Du
 */
public class LeftFragment extends Fragment {

    private static final String TAG = "LeftFragment";

    @Nullable
    @Override
    public View onCreateView(@NonNull LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
        @SuppressLint("InflateParams") View view = inflater.inflate(R.layout.left_fragment, null, false);
        Button btnTip = view.findViewById(R.id.btn_tip);
        TextView tvMessageOne = view.findViewById(R.id.tv_message_one);
        Bundle bundle = this.getArguments();
        if (bundle != null) {
            String message = bundle.getString("message");
            tvMessageOne.setText(message);
        }
        return view;
    }

    @Override
    public void onActivityCreated(@Nullable Bundle savedInstanceState) {
        super.onActivityCreated(savedInstanceState);
    }

    @Override
    public void onAttach(@NonNull Context context) {
        super.onAttach(context);
    }

    @Override
    public void onCreate(@Nullable Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
    }

    @Override
    public void onStart() {
        super.onStart();
    }

    @Override
    public void onResume() {
        super.onResume();
    }

    @Override
    public void onPause() {
        super.onPause();
    }

    @Override
    public void onStop() {
        super.onStop();
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
    }

    @Override
    public void onDestroyView() {
        super.onDestroyView();
    }

    @Override
    public void onDetach() {
        super.onDetach();
    }

    public void sendMessage(ICallBack callBack) {
        callBack.getMessageFromFragment("消息:我来自Fragment");
    }
}
